    fun run() : Int {
        val lines = input.split("\n")

        val RIGHT = 3
        val DOWN = 1

        val maxY: Int = lines.size

        var x = 3
        var y = 1

        var count = 0

        while (true) {
            if (lines[y][x] == '#') count++
            y += DOWN
            if (y == maxY) break

            x += RIGHT
            if (x > lines[y].length - 1) {
                x -= lines[y].length
            }
        }

        return count
    }

    fun run2() : Long {
        val lines = input.split("\n")

        val maxY: Int = lines.size

        var slopes = arrayOf(Pair(1, 1), Pair(3, 1), Pair(5, 1), Pair(7, 1), Pair (1, 2))

        var result : Long = 0
        slopes.forEach {
            var count : Long = 0
            var x = it.first
            var y = it.second
            while (true) {
                if (lines[y][x] == '#') count++
                y += it.second
                if (y >= maxY) break

                x += it.first
                if (x > lines[y].length - 1) {
                    x -= lines[y].length
                }
            }
            println("Slope $it: $count")
            if (result == 0L) {
                result = count
            } else {
                result *= count
            }
        }

        return result
    }
